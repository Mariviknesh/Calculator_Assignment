package org.example;

class Calculator{
    int a;
    int b;
    Calculator()
    {
        this.a=0;
        this.b=0;
    }

    public void setA(int a) {
        this.a = a;
    }
    public int getA(){
        return this.a;
    }
    public void setB(int b) {
        this.b = b;
    }
    public int getB(){
        return this.b;
    }

}
